namespace TheNote\core\command;

use pocketmine\network\mcpe\protocol\LevelSoundEventPacket;
use pocketmine\player\Player;
use pocketmine\Server;
use TheNote\core\Main;
use pocketmine\command\Command;
use pocketmine\command\CommandSender;
use pocketmine\utils\Config;

class HealCommand extends Command
{

    private $plugin;

    public function __construct(Main $plugin)
    {
        $this->plugin = $plugin;
        $langsettings = new Config($this->plugin->getDataFolder() . Main::$lang . "LangConfig.yml", Config::YAML);
        $l = $langsettings->get("Lang");
        $lang = new Config($this->plugin->getDataFolder() . Main::$lang . "Lang_" . $l . ".json", Config::JSON);
        $config = new Config($this->plugin->getDataFolder() . Main::$setup . "settings" . ".json", Config::JSON);
        parent::__construct("heal", $config->get("prefix") . $lang->get("healprefix"), "/heal");
        $this->setPermission("core.command.heal");
    }

    public function execute(CommandSender $sender, string $commandLabel, array $args): bool
    {
        $langsettings = new Config($this->plugin->getDataFolder() . Main::$lang . "LangConfig.yml", Config::YAML);
        $l = $langsettings->get("Lang");
        $lang = new Config($this->plugin->getDataFolder() . Main::$lang . "Lang_" . $l . ".json", Config::JSON);
        $config = new Config($this->plugin->getDataFolder() . Main::$setup . "settings" . ".json", Config::JSON);
        if (!$sender instanceof Player) {
            $sender->sendMessage($config->get("error") . $lang->get("commandingame"));
            return false;
        }
        if (!$this->testPermission($sender)) {
            $sender->sendMessage($config->get("error") . $lang->get("nopermission"));
            return false;
        }
        if (isset($args[0])) {
            if ($sender->hasPermission("core.command.heal.other")) {
                $victim = $this->plugin->getServer()->getPlayerExact($args[0]);
                $target = Server::getInstance()->getPlayerExact(strtolower($args[0]));
                if ($target == null) {
                    $sender->sendMessage($config->get("error") . $lang->get("playernotonline"));
                    return false;
                } else {
                    $sender->setAllowFlight(true);
                    $sender->setHealth(20);
                    $message = str_replace("{sender}" , $sender->getNameTag(), $lang->get("healtargetsucces"));
                    $target->sendMessage($config->get("prefix") . $message);
                    $message1 = str_replace("{victim}" , $victim->getName(), $lang->get("healtargetsucces2"));
                    $sender->sendMessage($config->get("prefix") . $message1);
                    return false;
                }
            } else {
                $sender->sendMessage($config->get("error") . $lang->get("healtargetnoperm"));
                return false;
            }
        }
        $sender->setHealth(20);
        $sender->sendMessage($config->get("prefix") . $lang->get("healsucces"));
        return false;
    }
}