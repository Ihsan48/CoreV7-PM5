namespace TheNote\core\command;

use pocketmine\event\Listener;
use pocketmine\player\Player;
use TheNote\core\Main;
use pocketmine\command\Command;
use pocketmine\command\CommandSender;
use pocketmine\utils\Config;

class ItemIDCommand extends Command implements Listener
{
    private $plugin;

    public function __construct(Main $plugin)
    {
        $this->plugin = $plugin;
        $langsettings = new Config($this->plugin->getDataFolder() . Main::$lang . "LangConfig.yml", Config::YAML);
        $l = $langsettings->get("Lang");
        $lang = new Config($this->plugin->getDataFolder() . Main::$lang . "Lang_" . $l . ".json", Config::JSON);
        $config = new Config($this->plugin->getDataFolder() . Main::$setup . "settings" . ".json", Config::JSON);
        parent::__construct("id", $config->get("prefix") . $lang->get("idprefix"), "/id");
        $this->setPermission("core.command.id");
    }

    public function execute(CommandSender $sender, string $commandLabel, array $args): bool
    {
        $langsettings = new Config($this->plugin->getDataFolder() . Main::$lang . "LangConfig.yml", Config::YAML);
        $l = $langsettings->get("Lang");
        $lang = new Config($this->plugin->getDataFolder() . Main::$lang . "Lang_" . $l . ".json", Config::JSON);
        $config = new Config($this->plugin->getDataFolder() . Main::$setup . "settings" . ".json", Config::JSON);
        if (!$sender instanceof Player) {
            $sender->sendMessage($config->get("error") . $lang->get("commandingame"));
            return false;
        }
        if (!$this->testPermission($sender)) {
            $sender->sendMessage($config->get("error") . $lang->get("nopermission"));
            return false;
        }
        $item = $sender->getInventory()->getItemInHand();
        $message = str_replace("{name}" , $sender->getNameTag() , $lang->get("idmessage"));
        $message1 = str_replace("{id}" , $item->getVanillaName() , $message);
        $sender->sendMessage($config->get("prefix") . $message1);
        $sender->sendMessage($config->get("prefix") . "§6Dein Item in der Hand ist §f:§e " . $item->getTypeId() . "§f:§e" . $item->getVanillaName());
        return true;
    }
}